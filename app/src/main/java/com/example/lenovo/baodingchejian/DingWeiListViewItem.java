package com.example.lenovo.baodingchejian;

public class DingWeiListViewItem {
    private String biaoti;
    private String gongli;

    public DingWeiListViewItem(String biaoti, String gongli) {
        this.biaoti = biaoti;
        this.gongli = gongli;
    }

    public String getBiaoti() {
        return biaoti;
    }

    public void setBiaoti(String biaoti) {
        this.biaoti = biaoti;
    }

    public String getGongli() {
        return gongli;
    }

    public void setGongli(String gongli) {
        this.gongli = gongli;
    }
}
